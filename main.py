def greaterThan(x,y):
    if x > y:
         return True
    else:
         return False

print(greaterThan(5,2))

def lessThan(x,y):
    if x < y:
        return True
    else:
        return False

print(lessThan(4,5))

def equalTo(x,y):
    if x == y:
        return True
    else:
        return False

print(equalTo(8,8))

